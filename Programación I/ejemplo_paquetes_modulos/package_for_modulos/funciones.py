def sumar(primer_numero: int, segundo_numero: int) -> int:
    # _descripcion_
    #
    #    Argumento:
    #      primer_numero [int] -> int -> numero ingresado por el usuario
    #      segundo_numero [int] -> int -> numero ingresado por el usuario
    #    Retorna:
    #      retorna -> suma
    
    suma = primer_numero + segundo_numero
    
    return suma